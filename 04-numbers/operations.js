// Example of arithmetic operations in JavaScript:
const x = 30;
const y = 20;

let result;

// Addition
result = x + y;
console.log(result);

// Subtraction
result = x - y;
console.log(result);

//Multiplication
result = x * y;
console.log(result);

//Division
result = x / y;
console.log(result);

//Module
result = x % y;
console.log(result);
