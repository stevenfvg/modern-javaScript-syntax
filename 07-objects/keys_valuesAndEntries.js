const product = {
  name: '20 Inch Monitor',
  price: 300,
  available: true,
};

// Example of how to use the keys method to see the properties of an object
console.log(Object.keys(product));

// Using the values method to see the values contained in the properties of an object
console.log(Object.values(product));

// Using the entries method to view the properties and values of an object
console.log(Object.entries(product));
