// Basic examples with Objects
const name = '20 Inch Monitor';
const price = 300;
const available = true;

// An object groups everything into a single variable. Example:

// Object literal
const product = {
  name: '20 Inch Monitor',
  price: 300,
  available: true,
};

console.log(product);

// Access object properties with dot syntax
console.log(product.name);
console.log(product.price);
console.log(product.available);

// Another way to access object properties
console.log(product['name']);
