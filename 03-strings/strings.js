// There are three ways to declare a string variable in JavaScript.

// Option 1
const productOne = '20 inch monitor';
console.log(productOne);

// Option 2
const productTwo = String('24 inch monitor');
console.log(productTwo);

// Option 3
const productThree = new String('27 inch monitor');
console.log(productThree);

// However, the most used option and the most recommended to use is option 1.